/**
 * Get terminal theme by name
 * Returns default theme if name is not found
 */
export function getTerminalTheme(name: TerminalTheme): TerminalThemeColors {
  return TERMINAL_THEMES[name] || TERMINAL_THEMES.default;
}

/**
 * Convert terminal theme to xterm.js ITheme format
 */
export function toXtermTheme(theme: TerminalThemeColors) {
  return {
    background: theme.background,
    foreground: theme.foreground,
    cursor: theme.cursor,
    cursorAccent: theme.cursorAccent,
    selectionBackground: theme.selectionBackground,
    black: theme.black,
    red: theme.red,
    green: theme.green,
    yellow: theme.yellow,
    blue: theme.blue,
    magenta: theme.magenta,
    cyan: theme.cyan,
    white: theme.white,
    brightBlack: theme.brightBlack,
    brightRed: theme.brightRed,
    brightGreen: theme.brightGreen,
    brightYellow: theme.brightYellow,
    brightBlue: theme.brightBlue,
    brightMagenta: theme.brightMagenta,
    brightCyan: theme.brightCyan,
    brightWhite: theme.brightWhite,
  };
}

/**
 * Get all available theme names
 */
export function getAvailableThemes(): TerminalTheme[] {
  return Object.keys(TERMINAL_THEMES) as TerminalTheme[];
}

